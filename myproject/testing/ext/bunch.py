class Bunch(dict):
    """ A dictionary that allows attribute-style access to its items.
    """

    def __init__(self, *args:'any_', **kwargs:'any_') -> 'None':
        super().__init__(*args, **kwargs)
        self.__dict__ = self

# ################################################################################################################################

    def __getattr__(self, name:'str') -> 'any_':
        try:
            return self[name]
        except KeyError:
            raise AttributeError(name)

# ################################################################################################################################

    def __setattr__(self, name:'str', value:'any_') -> 'None':
        if name == '_Bunch__dict__':
            super().__setattr__('__dict__', value)
        else:
            self[name] = value

# ################################################################################################################################

    def __delattr__(self, name:'str') -> 'None':
        try:
            del self[name]
        except KeyError:
            raise AttributeError(name)

# ################################################################################################################################

    def copy(self) -> 'Bunch':
        out = Bunch(super().copy())
        return out

# ################################################################################################################################
# ################################################################################################################################

def bunchify(data:'any_') -> 'any_':
    """ Recursively converts a dictionary into a Bunch.
    """
    if isinstance(data, dict):
        return Bunch({key: bunchify(value) for key, value in data.items()})
    elif isinstance(data, (list, tuple)):
        return type(data)(bunchify(item) for item in data)
    return data

# ################################################################################################################################
# ################################################################################################################################

__all__ = ['Bunch', 'bunchify']
